package com.example.demo;

public class Participate2 {
	
	private String sname;
	private String spname;
	
	
	public Participate2() {
		super();
	}
	public Participate2(String sname, String spname) {
		super();
		this.sname = sname;
		this.spname = spname;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSpname() {
		return spname;
	}
	public void setSpname(String spname) {
		this.spname = spname;
	}
	
	
	
	

}
