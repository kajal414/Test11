package com.example.demo;

public class Participate {
	
	private int pid;
	private int spid;
	
	public Participate() {
		super();
	}
	public Participate(int pid, int spid) {
		super();
		this.pid = pid;
		this.spid = spid;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getSpid() {
		return spid;
	}
	public void setSpid(int spid) {
		this.spid = spid;
	}
	
	
	

}
